const express = require('express');
const cors = require('cors');
const bodyParser = require('body-parser');
const helmet = require('helmet');
const rateLimit = require('./middleware/rateLimitMiddleware');
const validation = require('./middleware/validationMiddleware');
const logging = require('./middleware/loggingMiddleware');

const app = express();
const PORT = 5001;

// Security middleware
app.use(helmet()); // Add security headers

// CORS configuration
app.use(cors({
  origin: ['http://localhost:5173', 'http://localhost:3000'],
  credentials: true
}));

app.use(bodyParser.json({ limit: '50mb' }));
app.use(bodyParser.urlencoded({ limit: '50mb', extended: true }));

// Logging middleware
app.use(logging.requestLogger);

// Routes
app.use('/api', require('./routes/auth'));
app.use('/api', require('./routes/publications'));
app.use('/api/faculty', require('./routes/faculty'));
app.use('/api', require('./routes/papers'));
app.use('/api/insights', require('./routes/insights'));
app.use('/api', require('./routes/sdg'));
app.use('/api', require('./routes/homeStats'));
app.use("/admin", require('./routes/admin.routes'));
app.use('/api', require('./routes/monthlyReport'));

// Search and Export routes
app.use('/api/search', require('./routes/search'));
app.use('/api/export', require('./routes/export'));
app.use('/api/password', require('./routes/password'));




// Test API route
app.get('/api/test', (req, res) => {
  res.json({ message: 'API is working!' });
});

// Health check endpoint
app.get('/health', (req, res) => {
  res.json({ status: 'healthy', timestamp: new Date().toISOString() });
});

// 404 handler
app.use((req, res) => {
  res.status(404).json({ success: false, message: 'Route not found' });
});

// Global error handler
app.use((err, req, res, next) => {
  logging.errorLog(err, { 
    path: req.path, 
    method: req.method,
    ip: req.ip 
  });
  
  // Rate limit error
  if (err.status === 429) {
    return res.status(429).json({ 
      success: false, 
      message: err.message || 'Too many requests' 
    });
  }
  
  // Validation error
  if (err.status === 400) {
    return res.status(400).json({ 
      success: false, 
      message: err.message || 'Bad request' 
    });
  }
  
  // Default error
  res.status(500).json({ 
    success: false,
    message: 'Internal server error',
    // Only show detailed error in development
    ...(process.env.NODE_ENV === 'development' && { error: err.message })
  });
});

app.listen(PORT, () => {
  console.log(`ðŸš€ Server running on port ${PORT}`);
  console.log(`ðŸ“Š Environment: ${process.env.NODE_ENV || 'development'}`);
});
